package com.example.learnkotlin.native_kotlin.operations

fun main(args: Array<String>) {
    var x = 12

    println("+x = " + +x)
    println("x = $x")

    println("++x = " + ++x)
    println("x = $x")

    println("x++ = " + x++)
    println("x = $x")

    println("-x = " + -x)
    println("x = $x")

    println("--x = " + --x)
    println("x = $x")

    println("x-- = " + x--)
    println("x = $x")

    var isOkay = true
    println("!isOkay = " + !isOkay)


}