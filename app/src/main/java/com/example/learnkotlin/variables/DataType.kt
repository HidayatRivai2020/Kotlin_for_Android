package com.example.learnkotlin.variables

fun main(args: Array<String>) {
    var a:Boolean = true
    var b:Char = 'R'
    var c:Byte = 12
    var d:Short = -356
    var e:Int = 123564
    var f:Long = 1234354324L
    var g:Float = 1.4325253f
    var h:Double = 12.3124551341245

    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)

}